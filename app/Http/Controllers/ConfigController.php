     */
    public function store(Request $request)
    {

        if ($request->id_tbl=='') {
            $dt=new Configuracion();
        }else{
            $dt=Configuracion::find($request->id_tbl);
        }
        $dt->nombre=$request->nombre;
        $dt->color=$request->color;
        $dt->save();
        return redirect()->route('configs.index');
    }

